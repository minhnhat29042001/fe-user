export const loadProvinces = async () => {
  const url = `https://provinces.open-api.vn/api/p`;
  try {
    const res = await fetch(url, {
      method: 'GET'
    });
    if (res.status === 200) {
      const json = await res.json();
      return json;
    }
  } catch (error) {
    console.log(error);
  }
};
